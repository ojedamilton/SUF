<script>
    window.onload = ()=>{
        // Hacer una peticion get para obtener las empresas
            axios.get('http://localhost:8000/api/empresas')
            .then( (response)=>{
                var select = document.getElementById('empresa');
                var option = document.createElement('option');
                let empresas = response.data.empresas.data;
                empresas.forEach(empresa => {
                    var option = document.createElement('option');
                    option.value = empresa.id;
                    option.text = empresa.nombreEmpresa;
                    select.appendChild(option);
                })
            })
            .catch((error)=>{
                console.log(error);
            });
    };
</script>
